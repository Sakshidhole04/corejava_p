//Autoboxing
package com.java3.wrapper;

public class Prog3 {

	public static void main(String[] args) 
	{
	    //Integer
		int a=10;
		Integer A =a;
		System.out.println(A);
		
        //Double 
		double d=13.2;
		Double D=d;
		System.out.println(D);
		
		//float
		float f=3.6f;
		Float F=f;
		System.out.println(F);
		
		//Character 
		char c='d';
		Character C=c;
		System.out.println(c);
		
		//boolean 
		boolean b=true;
		Boolean B=b;
		System.out.println(B);
		
		//byte
		byte b1=5;
		Byte B1=b1;
		System.out.println(B1);
		
	}

}
